    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        int n=Integer.parseInt(txtcampoNumero.getText());
        for (int i = 0; i < n; i++) {
            lista.add(aleatorio(1, 30));
        }
        txainicial.setText(mostrar(lista));
    }//GEN-LAST:event_btngrabarActionPerformed

    private void btnmostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmostrarActionPerformed
        ArrayList<Integer> listaNueva=new ArrayList<Integer>();
        int cont=0;
        for (Integer l : lista) {
            if(l<=10){
                listaNueva.add(0);
                cont++;
            }else if(l>=20){
                listaNueva.add(1);
                cont++;
                
            }else{
                listaNueva.add(l);
            }
        }
        txafinal.setText(mostrar(listaNueva));
        txtsincambiar.setText(String.valueOf(lista.size()-cont));
    }//GEN-LAST:event_btnmostrarActionPerformed

